#include "./minishell.h"

int	builtin_pwd(t_shell *sh)
{
	char	pwd[2048];

	if (getcwd(pwd, sizeof(char) * 2048))
	{
		ft_putstr_fd(pwd, 1);
		ft_putstr_fd("\n", 1);
		sh->status = 0;
	}
	return (1);
}
