#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*s2;

	s2 = 0;
	while (*s)
	{
		if (*s == c)
			s2 = (char*)s;
		s++;
	}
	if (s2)
		return (s2);
	if (c == '\0')
		return ((char*)s);
	return (0);
}
